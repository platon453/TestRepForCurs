const formatSize = (bytes) => {
    if (bytes <= 1024) {
        return (`${bytes} B`);
    }

    const mbytes = (bytes / 1024 / 1024).toFixed(2);
    return (`${mbytes} MB`);
};

const fileSize = [500, 5000000, 500000, 10000, 1024];

for (const size of fileSize) {
    console.log(formatSize(size));
} 